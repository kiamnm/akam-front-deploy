const akmDictionary=(word)=>{
    if(word==="میلگرد"){
        return "bar"
    }
    if(word==="ساده"){
        return "plain"
    }
    if(word==="آجدار"){
        return "deformedbar"
    }
    if(word==="bar"){
        return "میلگرد"
    }
    if(word==="plain"){
        return "ساده"
    }
    if(word==="deformedbar"){
        return "آجدار"
    }
}

export default akmDictionary